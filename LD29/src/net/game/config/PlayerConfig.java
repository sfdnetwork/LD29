package net.game.config;

public class PlayerConfig {

	public static int PlayerX;
	public static int PlayerY;
	public static int PlayerWidth;
	public static int PlayerHeight;
	
	public static int getPlayerX() {
		return PlayerX;
	}
	public static void setPlayerX(int playerX) {
		PlayerX = playerX;
	}
	public static int getPlayerY() {
		return PlayerY;
	}
	public static void setPlayerY(int playerY) {
		PlayerY = playerY;
	}
	public static int getPlayerWidth() {
		return PlayerWidth;
	}
	public static void setPlayerWidth(int playerWidth) {
		PlayerWidth = playerWidth;
	}
	public static int getPlayerHeight() {
		return PlayerHeight;
	}
	public static void setPlayerHeight(int playerHeight) {
		PlayerHeight = playerHeight;
	}
	
	
	
	
}
